# Solicita a primeira string ao usuário
primeira_string = input("Digite a primeira string: ")

# Solicita a segunda string ao usuário
segunda_string = input("Digite a segunda string: ")

# Concatena as duas strings usando o operador '+'
# Adicionamos um espaço entre elas para melhor legibilidade
string_concatenada = primeira_string + " " + segunda_string

# Apresenta a string resultante no terminal
print("\n--- Resultado da Concatenação ---")
print("A string final é:", string_concatenada)
print("-----------------------------------")